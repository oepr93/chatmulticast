    public void recibirConversacion(Socket elSocket, String usuario,String elMensaje)
    {
                    boolean yaEsta=false;
                    Component porsiyaesta = null;
                    for(int i = 0;i<jTabbedPane1.getTabCount();i++)
                    {
                        Component Pestanai = jTabbedPane1.getComponent(i);
                        if(Pestanai != null)
                        {
                            String nombre = Pestanai.getName();
                            if(nombre.equals(usuario.toString()))
                            {
                                porsiyaesta = Pestanai;
                                yaEsta=true;
                            }
                        }

                    }
                    if(!yaEsta)
                    {
                        Pestana miPestana = new Pestana(elSocket, usuario.toString());
                        miPestana.setName(usuario.toString());
                        jTabbedPane1.addTab(usuario.toString(), miPestana);
                        miPestana.MostrarMensaje(elMensaje);
                        miPestana.setBackground(Color.red);
                    }else{
                        Pestana pesTemp = (Pestana)porsiyaesta;
                        pesTemp.MostrarMensaje(elMensaje);
                        pesTemp.setBackground(Color.red);
                    }

    }
