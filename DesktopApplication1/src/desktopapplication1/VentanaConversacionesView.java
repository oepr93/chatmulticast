    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged

            if (!evt.getValueIsAdjusting())
            {
                try {
                    JList laLista = (JList) evt.getSource();
                    Object usuario = laLista.getSelectedValue();
                    boolean yaEsta=false;
                    for(int i = 0;i<jTabbedPane1.getTabCount();i++)
                    {
                        Component Pestanai = jTabbedPane1.getComponent(i);
                        if(Pestanai != null)
                        {
                            String nombre = Pestanai.getName();
                            if(nombre.equals(usuario.toString()))
                            {
                                yaEsta=true;
                            }
                        }

                    }
                    if(!yaEsta)
                    {
                        Vector<Usuario> llstUsuarios = elChat.getUsuariosConectados();
                        for(Usuario usrTemp: llstUsuarios)
                        {
                            if(usrTemp.getNombre().equals(usuario.toString()))
                            {
                                Socket elSocket = new Socket(usrTemp.getIp(), 4446);
                                Pestana miPestana = new Pestana(elSocket, usuario.toString());
                                miPestana.setName(usuario.toString());
                                jTabbedPane1.addTab(usuario.toString(), miPestana);
                                break;
                            }
                        }
                        
                    }
                    
                } catch (UnknownHostException ex) {
                    Logger.getLogger(VentanaConversacionesView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaConversacionesView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        

    }//GEN-LAST:event_jList1ValueChanged

